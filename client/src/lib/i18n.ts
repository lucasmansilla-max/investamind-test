export const useTranslation = () => {
  const language = localStorage.getItem('selectedLanguage') || 'en';

  const t = (key: string) => {
    const keys = key.split('.');
    let value: any = translations[language as keyof typeof translations];

    for (const k of keys) {
      value = value?.[k];
    }

    return value || key;
  };

  return { t, language };
};
